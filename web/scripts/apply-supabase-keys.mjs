/**
 * Reads Supabase keys from assets/keys/SupabaseKeys.txt or kets/SupabaseKeys.txt
 * and writes web/.env for Vite.
 *
 * Supported formats:
 *   VITE_SUPABASE_URL=https://xxxx.supabase.co
 *   VITE_SUPABASE_ANON_KEY=eyJ...   OR   sb_publishable_...
 *
 * Or dashboard-style labels:
 *   Project URL: https://xxxx.supabase.co
 *   Publishable Key: sb_publishable_...
 *
 * Never put the Secret / service key in .env — client only uses URL + publishable (or legacy anon).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '..', '..')
const outFile = path.resolve(__dirname, '..', '.env')

const candidates = [
  path.join(repoRoot, 'assets', 'keys', 'SupabaseKeys.txt'),
  path.join(repoRoot, 'kets', 'SupabaseKeys.txt'),
]

let src = candidates.find((p) => fs.existsSync(p))
if (!src) {
  console.error(
    'No keys file found. Create one of:\n',
    candidates.map((p) => '  - ' + p).join('\n'),
  )
  process.exit(1)
}

const raw = fs.readFileSync(src, 'utf8')
const lines = raw.split(/\r?\n/)

let url = ''
let anon = ''

for (const line of lines) {
  const t = line.trim()
  if (!t || t.startsWith('#')) continue

  let m = t.match(/^VITE_SUPABASE_URL=(.*)$/i)
  if (m) {
    url = m[1].trim()
    continue
  }
  m = t.match(/^Project\s+URL:\s*(.*)$/i)
  if (m) {
    url = m[1].trim()
    continue
  }

  m = t.match(/^VITE_SUPABASE_ANON_KEY=(.*)$/i)
  if (m) {
    anon = m[1].trim()
    continue
  }
  m = t.match(/^Publishable\s+Key:\s*(.*)$/i)
  if (m) {
    anon = m[1].trim()
    continue
  }

  m = t.match(/^Secret\s+Key:\s*(.*)$/i)
  if (m) {
    console.warn(
      '[apply-supabase-keys] Ignoring Secret Key line (do not use in the web app).',
    )
    continue
  }
}

if (!url || !anon) {
  const missing = [!url && 'Project URL', !anon && 'Publishable or anon key']
    .filter(Boolean)
    .join(' and ')
  console.error(
    `Missing ${missing} in:\n  ${src}\n\n` +
      'From Supabase → Project Settings → API, add a line:\n' +
      '  Project URL: https://YOUR_REF.supabase.co\n' +
      'and a publishable key line:\n' +
      '  Publishable Key: sb_publishable_...\n\n' +
      'Or use VITE_SUPABASE_URL= and VITE_SUPABASE_ANON_KEY=.\n',
  )
  process.exit(1)
}

if (anon.startsWith('sb_secret_')) {
  console.error(
    'The Secret key must not be used in the browser. Use Publishable (sb_publishable_...) or legacy anon key.',
  )
  process.exit(1)
}

if (!url.startsWith('http')) {
  console.error('Project URL / VITE_SUPABASE_URL must start with https://')
  process.exit(1)
}

const envBody =
  `# Generated by npm run env:apply — do not commit (see .gitignore)\n` +
  `VITE_SUPABASE_URL=${url}\n` +
  `VITE_SUPABASE_ANON_KEY=${anon}\n`

fs.writeFileSync(outFile, envBody, 'utf8')
console.log('Wrote', outFile)
console.log('Restart the dev server (npm run dev) if it is already running.')
